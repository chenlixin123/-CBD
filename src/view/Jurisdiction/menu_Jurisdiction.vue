  created() {
    console.log(url.time(1556553600000));
    this.list();
    this.menuList();
  },
  methods: {
    //改变表格上级菜单的颜色
    rowClassName(row, index) {
      //   console.log(row.parentId);
      if (row.parentId === 0) {
        return "demo-table-info-row";
      }
      return "";
    },
    change(id) {
      //   console.log(id);
      //   console.log(this.top_menu);
      this.top_menu.map(res => {
        console.log(res);
        if (res.id == id) {
          this.success_data.parentName = res.menuName;
        }
      });
    },
    //table列表
    list() {
      let that = this;
      this.loadinges = true;
      this.data = "";
      this.tableList.pageNo = 1;
      this.totalPages = 0;
      axios1
        .request({
          url: url.url.search_auth_ment,
          params: this.tableList,
          method: "post"
        })
        .then(res => {
          console.log(res);
          that.spinShow = false;
          if (res.data.data.list.length == 0) {
            this.loadingText = "";
          } else {
            this.data = res.data.data;
            this.totalPages = res.data.data.totalPages;
          }
          this.loadinges = false;
        });
    },
    menuList() {
      axios
        .request({
          url: url.url.search_root_menu,
          method: "get"
        })
        .then(res => {
          console.log(res.data.data);
          this.top_menu = res.data.data;
        });
    },
    //取消删除
    canceles_delete() {
      this.show_delete = false;
      this.loading_delete = false;
      this.id_delete = "";
    },
    //删除角色信息
    delete(id) {
      console.log("删除");
      console.log(id);
      this.show_delete = true;
      this.id_delete = id;
    },

    success_delete() {
      this.loading_delete = true;
      axios
        .request({
          url: url.url.delete_menu,
          params: {
            id: this.id_delete
          },
          method: "delete"
        })
        .then(res => {
          console.log(res);
          if (res.data.code == 200) {
            this.$Notice.success({
              title: "系统提示",
              desc: res.data.message
            });
            this.list();
            this.menuList();
          } else {
            this.$Notice.error({
              title: "系统提示",
              desc: res.data.message
            });
          }
          this.loading_delete = false;
          this.show_delete = false;
        });
    },
    //跳转第几页
    search(data) {
      this.data = "";
      this.loadinges = true;
      console.log(data - 1);
      this.pages = data;
      let that = this;
      this.tableList.pageNo = data;
      axios1
        .request({
          url: url.url.search_auth_ment,
          params: this.tableList,
          method: "post"
        })
        .then(res => {
          console.log(res);
          this.numbers = data;
          if (res.data.data.list.length == 0) {
            this.loadingText = "";
          } else {
            this.data = res.data.data;
            this.totalPages = res.data.data.totalPages;
          }
          this.loadinges = false;
        });
    },
    number(data) {
      if (data == "") {
        this.numbers = data;
      } else {
        var reg = /^\d{1,}$/;
        console.log(reg.test(data));
        if (reg.test(data) == false) {
          this.numbers = 1;
        } else {
          if (data >= 1) {
            this.numbers = data;
          } else {
            this.numbers = 1;
          }
        }
      }
    },
    //点击跳转
    jump() {
      console.log(this.totalPages);
      this.data = "";
      this.loadinges = true;
      let that = this;
      if (this.numbers == "") {
        this.numbers = 1;
      } else if (this.numbers > this.totalPages) {
        this.numbers = this.totalPages;
        console.log(this.totalPages);
      }
      console.log(this.numbers);
      this.tableList.pageNo = this.numbers;
      // console.log(this.tableList);
      axios1
        .request({
          url: url.url.search_auth_ment,
          params: this.tableList,
          method: "post"
        })
        .then(res => {
          that.pages = Number(this.numbers);
          console.log(res);
          that.spinShow = false;
          if (res.data.data.list.length == 0) {
            this.loadingText = "";
          } else {
            this.data = res.data.data;
            this.totalPages = res.data.data.totalPages;
          }
          this.loadinges = false;
        });
    },
    del() {
      this.modal10 = false;
      this.button_loading = false;
    },
    //新建角色
    success_modal() {
      this.button_loading = true;
      if (!this.success_data.menuName) {
        this.button_loading = false;
        this.$Message.warning("请输入名称");
      } else if (!this.success_data.menuCode) {
        this.button_loading = false;
        this.$Message.warning("请输入编码");
      } else if (!this.success_data.url) {
        this.button_loading = false;
        this.$Message.warning("请输入URL");
      } else if (!this.success_data.sort) {
        this.button_loading = false;
        this.$Message.warning("请输入排序字段");
      } else {
        if (!this.success_data.parentId) {
          this.success_data.parentId = 0;
        }
        if (this.title_modau == "新增系统资源") {
          console.log("新建角色");
          console.log(this.success_data);
