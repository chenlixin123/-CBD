        condition: {
          bindType: 0,
          menuName: "",
          roleId: 0,
          state: ""
        },
        pageNo: 1,
        pageSize: 10
      },
      data: "",
      show: "",
      columns: [
        {
          title: "菜单编码",
          align: "center",
          //   sortable: 'custom',
          key: "menuCode",
          tooltip: "true",
          minWidth: 100
        },
        {
          title: "菜单名称",
          align: "center",
          //   sortable: 'custom',
          key: "menuName",
          minWidth: 90
        },
        {
          title: "url",
          align: "center",
          //   sortable: 'custom',
          key: "url",
          minWidth: 90
        },
        {
          title: "状态",
          align: "center",
          //   sortable: 'custom',
          key: "state",
          minWidth: 90,
          render: (h, params) => {
            let data = params.row.state;
            data = data == 0 ? "禁用" : data == 1 ? "启用" : "";
            return h("div", {}, data);
            console.log(data);
          }
        },
        {
          title: "创建时间",
          align: "center",
          //   sortable: 'custom',
          key: "createTime",
          minWidth: 135,
          render: (h, params) => {
            let parking = params.row;
            if (parking.createTime) {
              parking.createTime = url.TT(parking.createTime);
            } else {
              parking.createTime = "";
            }

            // console.log(parking.createTime);
            return h("div", {}, parking.createTime);
          }
        },
        {
          title: "操作",
          key: "id",
          minWidth: 205,
          align: "center",
          render: (h, params) => {
            let parking = params.row;
            // console.log(parking);
            let menu = null;
            menu = h("div", [
              h("i-switch", {
                props: {
                  type: "warning",
                  size: "large",
                  // disabled:
                  //   params.row.fixedId === null || params.row.rentStatus == 3,
