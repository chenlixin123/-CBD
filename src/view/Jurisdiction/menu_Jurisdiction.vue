            .request({
              url: url.url.modify_auth_menu,
              data: this.success_data,
              method: "post"
            })
            .then(res => {
              console.log(res);
              if (res.data.code == 200) {
                this.$Notice.success({
                  title: "系统提示",
                  desc: res.data.message
                });
                this.list();
                this.menuList();
              } else {
                this.$Notice.error({
                  title: "系统提示",
                  desc: res.data.message
                });
              }
              this.button_loading = false;
              this.modal10 = false;
            });
        }
      }
    },
    //启用
